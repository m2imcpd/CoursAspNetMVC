        private void Button_Click(object sender, RoutedEventArgs e)
        {
            OpenFileDialog open = new OpenFileDialog();
            if(open.ShowDialog() == true)
            {  
                result.Text = "";
                foreach (Byte b in File.ReadAllBytes(open.FileName))
                {
                    result.Text += b;
                }
            }
        }
